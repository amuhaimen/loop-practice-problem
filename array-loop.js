// numbers array loop
var numbers = [10, 14, 22, 28, 33, 37, 45, 46, 50, 52, 56, 89, 30];

for (var i = 4; i < numbers.length; i++) {
  var number = numbers[i];
  console.log(number);
}
/**
 result:
 10,14,22,28,33,37,45,46,50,52,56,89,30
 */

